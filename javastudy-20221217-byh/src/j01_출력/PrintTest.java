package j01_출력;

public class PrintTest {

	public static void main(String[] args) {
		 System.out.println("Hello, Java!");
		 
		 System.out.print("변윤후");
		 System.out.print("-----");
		 
		 System.out.print("주말수업");
		 System.out.println("--");
		 
		 // 주석 : 프로그램에 영향을 주지 않는 글
//		 실행 단축기 : CTRL F11
//   	주석 단축기 : ctrl +/
//		 println 은 줄 바꿈해라 이다
		 
		 System.out.print("Hello, \nJava\n");
		 System.out.println("변윤후");
//		 \n : 줄바꿈
		 
		 int level;
		 level = 10;
		 System.out.println(level);
		 level = 11;
		 System.out.println(level);
	}

}
